#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	i;
	int	j;
	int	k;

	i = 0;
	j = 1;
	k = 0;
	while (nptr[i] == 32 || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			j = j * (-1);
		i++;
	}
	while (nptr[i] >= 48 && nptr[i] <= 57)
	{
		k = (k * 10) + (nptr[i] - 48);
		i++;
	}
	return (k * j);
}
